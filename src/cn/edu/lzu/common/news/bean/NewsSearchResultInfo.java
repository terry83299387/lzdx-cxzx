package cn.edu.lzu.common.news.bean;

public class NewsSearchResultInfo {
	private String newsCode;
	private String newsTitle;
	private String briefContent;
	private String createDate;
	private String newsSource;
	private String author;

	/**
	 * @return the newsCode
	 */
	public String getNewsCode() {
		return newsCode;
	}
	/**
	 * @param newsCode the newsCode to set
	 */
	public void setNewsCode(String newsCode) {
		this.newsCode = newsCode;
	}
	/**
	 * @return the newsTitle
	 */
	public String getNewsTitle() {
		return newsTitle;
	}
	/**
	 * @param newsTitle the newsTitle to set
	 */
	public void setNewsTitle(String newsTitle) {
		this.newsTitle = newsTitle;
	}
	/**
	 * @return the briefContent
	 */
	public String getBriefContent() {
		return briefContent;
	}
	/**
	 * @param briefContent the briefContent to set
	 */
	public void setBriefContent(String briefContent) {
		this.briefContent = briefContent;
	}
	/**
	 * @return the createDate
	 */
	public String getCreateDate() {
		return createDate;
	}
	/**
	 * @param createDate the createDate to set
	 */
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	/**
	 * @return the newsSource
	 */
	public String getNewsSource() {
		return newsSource;
	}
	/**
	 * @param newsSource the newsSource to set
	 */
	public void setNewsSource(String newsSource) {
		this.newsSource = newsSource;
	}
	/**
	 * @return the author
	 */
	public String getAuthor() {
		return author;
	}
	/**
	 * @param author the author to set
	 */
	public void setAuthor(String author) {
		this.author = author;
	}

}
